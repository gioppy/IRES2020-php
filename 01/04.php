<?php

//Si scriva un programma in PHP capace di compiere le 5 operazioni (somma, sottrazione, moltiplicazione, divisione e modulo)

$a = 10;
$b = 55;

// somma
print "La somma di $a e $b è " . ($a + $b) . "<br />";

// sottrazione
print "La sottrazione di $a e $b è " . ($a - $b) . "<br />";

// moltiplicazione
print "Il prodotto di $a e $b è " . ($a * $b) . "<br />";

// divisione
print "La divisione di $a e $b è " . ($a / $b) . "<br />";

// modulo
print "Il resto della divisione di $a e $b è " . ($a % $b) . "<br />";
